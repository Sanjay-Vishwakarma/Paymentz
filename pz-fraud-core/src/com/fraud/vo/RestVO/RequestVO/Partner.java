package com.fraud.vo.RestVO.RequestVO;

import javax.ws.rs.FormParam;
import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * Created by Admin on 3/13/2018.
 */
@XmlRootElement(name="partner")
@XmlAccessorType(XmlAccessType.FIELD)
public class Partner
{
    @FormParam("partner.username")
    String loginName;

    @FormParam("partner.password")
    String newPassword;

    @FormParam("partner.conPassword")
    String conPassword;

    @FormParam("partner.companyName")
    String companyName;

    @FormParam("partner.website")
    String website;

    @FormParam("partner.contactName")
    String contactName;

    @FormParam("partner.email")
    String email;


    public String getLoginName()
    {
        return loginName;
    }

    public void setLoginName(String loginName)
    {
        this.loginName = loginName;
    }

    public String getNewPassword()
    {
        return newPassword;
    }

    public void setNewPassword(String newPassword)
    {
        this.newPassword = newPassword;
    }

    public String getConPassword()
    {
        return conPassword;
    }

    public void setConPassword(String conPassword)
    {
        this.conPassword = conPassword;
    }

    public String getCompanyName()
    {
        return companyName;
    }

    public void setCompanyName(String companyName)
    {
        this.companyName = companyName;
    }

    public String getWebsite()
    {
        return website;
    }

    public void setWebsite(String website)
    {
        this.website = website;
    }

    public String getContactName()
    {
        return contactName;
    }

    public void setContactName(String contactName)
    {
        this.contactName = contactName;
    }

    public String getEmail()
    {
        return email;
    }

    public void setEmail(String email)
    {
        this.email = email;
    }
}
