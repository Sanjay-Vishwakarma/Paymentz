public class DefaultThemeVO
{
    String themeId;
    String defaultThemeName;
    String defaultThemeCreationTime;

    public String getThemeId()
    {
        return themeId;
    }

    public void setThemeId(String themeId)
    {
        this.themeId = themeId;
    }

    public String getDefaultThemeName()
    {
        return defaultThemeName;
    }

    public void setDefaultThemeName(String defaultThemeName)
    {
        this.defaultThemeName = defaultThemeName;
    }

    public String getDefaultThemeCreationTime()
    {
        return defaultThemeCreationTime;
    }

    public void setDefaultThemeCreationTime(String defaultThemeCreationTime)
    {
        this.defaultThemeCreationTime = defaultThemeCreationTime;
    }
}
